public class Skin 
{
	private String nome;
	private int rariedade;
	private double valor;
	private boolean possui;
	
	public String getNome() 
	{
		return nome;
	}
	public void setNome(String nome)
	{
		this.nome = nome;
	}
	public int getRariedade() 
	{
		return rariedade;
	}
	public void setRariedade(int rariedade) 
	{
		this.rariedade = rariedade;
	}
	public double getValor() 
	{
		return valor;
	}
	public void setValor(double valor) 
	{
		this.valor = valor;
	}
	public boolean isPossui() 
	{
		return possui;
	}
	public void setPossui(boolean possui) 
	{
		this.possui = possui;
	}
}
